#[derive(Debug, Clone)]
pub struct MetaballPoint {
    pub position: Point,
    pub radius: f64,
}

impl MetaballPoint {
    pub fn new(position: Point, radius: f64) -> MetaballPoint {
        MetaballPoint { position, radius }
    }

    pub fn default() -> MetaballPoint {
        MetaballPoint {
            position: Point::new(0.0, 0.0, 0.0),
            radius: 1.0,
        }
    }

    pub fn with_position(&self, position: Point) -> MetaballPoint {
        let mut point = self.clone();
        point.position = position;
        point
    }

    pub fn with_radius(&self, radius: f64) -> MetaballPoint {
        let mut point = self.clone();
        point.radius = radius;
        point
    }

    pub fn value(&self, point: &Point) -> f64 {
        fn inv_sqrt(x: f64) -> f64 {
            let x = x as f32;
            let i = x.to_bits();
            let i = 0x5f3759df - (i >> 1);
            let y = f32::from_bits(i);
        
            (y * (1.5 - 0.5 * x * y * y)) as f64
        }
        // Euclidian distance between both points
        let dist = *point - self.position;
        inv_sqrt(dist.x * dist.x + dist.y * dist.y + dist.z * dist.z) * self.radius
    }
}

impl Default for MetaballPoint {
    fn default() -> Self {
        MetaballPoint::default()
    }
}

#[derive(Debug, Clone)]
pub struct Metaball {
    pub bounds: (Point, Point),
    pub resolution: f64,
    pub points: Vec<MetaballPoint>,
    pub texture: Texture,
    pub fronteer: f64,
}

impl Metaball {
    pub fn new(
        bounds: (Point, Point),
        resolution: f64,
        points: Vec<MetaballPoint>,
        texture: Texture,
        fronteer: f64,
    ) -> Metaball {
        Metaball {
            bounds,
            resolution,
            points,
            texture,
            fronteer,
        }
    }

    pub fn default() -> Metaball {
        Metaball {
            bounds: (Point::new(-10.0, -10.0, -10.0), Point::new(10.0, 10.0, 10.0)),
            resolution: 0.1,
            points: vec![],
            texture: Texture::default(),
            fronteer: 1.0,
        }
    }

    pub fn with_bounds(&self, bounds: (Point, Point)) -> Metaball {
        Metaball {
            bounds,
            ..self.clone()
        }
    }

    pub fn with_resolution(&self, resolution: f64) -> Metaball {
        Metaball {
            resolution,
            ..self.clone()
        }
    }

    pub fn with_points(&self, points: Vec<MetaballPoint>) -> Metaball {
        Metaball {
            points,
            ..self.clone()
        }
    }

    pub fn with_point(&self, point: MetaballPoint) -> Metaball {
        let mut points = self.points.clone();
        points.push(point);
        Metaball { 
            points,
            ..self.clone()
        }
    }

    pub fn with_fronteer(&self, fronteer: f64) -> Metaball {
        Metaball {
            fronteer,
            ..self.clone()
        }
    }

    pub fn with_texture(&self, texture: Texture) -> Metaball {
        Metaball {
            texture,
            ..self.clone()
        }
    }

    pub fn value(&self, point: &Point) -> f64 {
        self.points.iter().fold(0.0, |acc, p| acc + p.value(point))
    }

    fn iter_points(&self) -> impl Iterator<Item = Point> + '_ {
        let nb_cells_x = ((self.bounds.1.x - self.bounds.0.x) / self.resolution).ceil() as usize;
        let nb_cells_y = ((self.bounds.1.y - self.bounds.0.y) / self.resolution).ceil() as usize;
        let nb_cells_z = ((self.bounds.1.z - self.bounds.0.z) / self.resolution).ceil() as usize;

        (0..nb_cells_x)
            .flat_map(move |x| {
                (0..nb_cells_y).flat_map(move |y| (0..nb_cells_z).map(move |z| (x, y, z)))
            })
            .map(move |(x, y, z)| {
                Point::new(
                    self.bounds.0.x + x as f64 * self.resolution,
                    self.bounds.0.y + y as f64 * self.resolution,
                    self.bounds.0.z + z as f64 * self.resolution,
                )
            })
    }

    fn cube_at(&self, point: &Point) -> Vec<Point> {
        [ 0, 1, 3, 2, 4, 5, 7, 6 ].iter()
            .map(|i| *point + Point::new(
                (i & 1) as f64 * self.resolution,
                ((i >> 1) & 1) as f64 * self.resolution,
                ((i >> 2) & 1) as f64 * self.resolution,
            )).collect()
    }

    fn iter_cubes(&self) -> impl Iterator<Item = Vec<Point>> + '_ {
        self.iter_points().map(move |p1| self.cube_at(&p1))
    }

    fn cube_value(&self, cube: &Vec<Point>) -> usize {
        cube.iter()
            .fold(0, |acc, p| acc * 2 + (self.value(p) < self.fronteer) as usize)
    }
}

impl Default for Metaball {
    fn default() -> Self {
        Metaball::default()
    }
}

impl MeshGenerator for Metaball {
    fn generate(&self) -> Mesh {
        let points = self.iter_cubes().fold(Vec::new(), |points, cube| {
            let cube_value = self.cube_value(&cube);

            if cube_value == 0 || cube_value == 255 {
                return points;
            }

            let config = TRI_TABLE[cube_value];

            let mut points = points;
            for i in 0..5 {
                let edge = config[i * 3];
                if edge == -1 {
                    break;
                }
                (0..3).for_each(|j| {
                    let edge = config[i * 3 + j];
                    let edge = EDGE_TABLE[edge as usize];
                    let p1 = cube[7 - edge[0]];
                    let p2 = cube[7 - edge[1]];
                    let value1 = self.value(&p1);
                    let value2 = self.value(&p2);
                    let t = (self.fronteer - value1) / (value2 - value1);
                    let p = p1 + (p2 - p1) * t;
                    points.push(p);
                });
            }

            points
        });
        
        let mesh = Mesh::default();

        // Compute all the normal triangles
        let tri = (0..points.len()).step_by(3).map(|i| {
            Triangle::default().with_a(points[i]).with_b(points[i + 1]).with_c(points[i + 2])
        }).collect::<Vec<_>>();

        let smooth_tri = tri.iter().map(|t| {
            let normals = t.points.iter().map(|p| {
                tri.iter()
                    .filter(|t| t.points.iter().any(|p2| (*p - *p2).length() < 1e-6))
                    .fold(
                        Vector::zero(),
                        |acc, t| acc + t.normal(p),
                    ).normalize()
            }).collect();
            SmoothTriangle::default().with_points(t.points).with_normals(normals).with_texture(self.texture.clone())
        });

        //mesh.with_faces(tri.iter().map(|t| t.clone().into()).collect())
        mesh.with_faces(smooth_tri.map(|t| t.into()).collect())
    }
}

impl From<Metaball> for Object {
    fn from(metaball: Metaball) -> Self {
        (&metaball.generate()).into()
    }
}

